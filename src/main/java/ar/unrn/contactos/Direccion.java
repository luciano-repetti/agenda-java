package ar.unrn.contactos;

public class Direccion {
    public String pais;
    public String provincia;
    public String ciudad;
    public String calle;

    public Direccion(String pais, String provincia, String ciudad, String calle) {
        this.pais = pais;
        this.provincia = provincia;
        this.ciudad = ciudad;
        this.calle = calle;
    }
}
